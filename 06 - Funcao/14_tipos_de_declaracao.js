//function declaration: carrega todas as funções declarada dessa forma antes de rodar o código
function soma(x, y){
    return (x + y);
}

//function expression: precisa ser declarada antes de usar, diferentemente da função soma
const sub = function(x, y){
    return (x - y);
}

//named function expression -> pouco ultilizada
//precisa ser declarada antes de usar, diferentemente da função soma
const mult = function mult(x, y){
    return (x * y);
}

