const imprmirOperacoes = function(x = 0, y = 0){
    console.log("Números:", x, "e", y);
    console.log("Soma =", x + y);
    console.log("Subtração =", x - y);
    console.log("Produto =", x * y);
    console.log("Quociente =", x / y);
    console.log("-");
}

imprmirOperacoes();
imprmirOperacoes(1, 2);